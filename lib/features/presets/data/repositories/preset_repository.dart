class PresetRepository {
  // Favorites tracking (in-memory, can be persisted later)
  static final Set<String> _favorites = {};

  // =============================================
  // HELPER METHODS
  // =============================================

  static bool isFavorite(String id) => _favorites.contains(id);

  static void toggleFavorite(String id) {
    if (_favorites.contains(id)) {
      _favorites.remove(id);
    } else {
      _favorites.add(id);
    }
  }

  static List<PresetModel> getFavoritePresets() {
    return getAllPresets().where((p) => _favorites.contains(p.id)).toList();
  }

  static List<PresetModel> getPresetsByCategory(PresetCategory category) {
    return getAllPresets().where((p) => p.category == category).toList();
  }

  static List<PresetModel> getFreePresets() {
    return getAllPresets().where((p) => !p.isPro).toList();
  }

  static List<PresetModel> getProPresets() {
    return getAllPresets().where((p) => p.isPro).toList();
  }

  static List<PresetModel> searchPresets(String query) {
    final lowerQuery = query.toLowerCase();
    return getAllPresets().where((p) =>
      p.name.toLowerCase().contains(lowerQuery) ||
      p.description.toLowerCase().contains(lowerQuery)
    ).toList();
  }

  static PresetModel? getPresetById(String id) {
    try {
      return getAllPresets().firstWhere((p) => p.id == id);
    } catch (_) {
      return null;
    }
  }

